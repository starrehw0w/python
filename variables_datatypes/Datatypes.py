#   Python Datatypes

#   In Python there are primarily two datatypes
#   
#   1. Primitive
#       - Boolean <class 'bool'>
#       - Integer <class 'int'>
#       - Float <class 'float'>
#       - String <class 'str'>
#
#   2. Non-Primitive
#       - Lists <class 'list'>
#       - Tuples <class 'tuple'>
#       - Sets <class 'set'>
#       - Dictionary <class 'dictionary'>

var_bool = True
print(type(var_bool), var_bool)

var_int = 1
print(type(var_int), var_int)

var_float = 1.0
print(type(var_float), var_float)

var_string = "One"
print(type(var_string), var_string)

var_list = list()
print(type(var_list), var_list)

var_tuple = tuple()
print(type(var_tuple), var_tuple)

var_set = set()
print(type(var_set), var_set)

var_dict = dict()
print(type(var_dict), var_dict)
